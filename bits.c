/* 
 * bitXor - x^y using only ~ and & 
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14
 *   Rating: 1
 */
int bitXor(int x, int y) {
  int l = ~x & y;
  int r = x & ~y;
  // Subsitution form of OR
  int l_inv = ~l;
  int r_inv = ~r;
  return ~(l_inv & r_inv);
}
/* 
 * bitMatch - Create mask indicating which bits in x match those in y
 *            using only ~ and & 
 *   Example: bitMatch(0x7, 0xfffffffE) = 0x6
 *   Legal ops: ~ & |
 *   Max ops: 14
 *   Rating: 1
 */
int bitMatch(int x, int y) {
  int l = x & y;
  int x_inv = ~x;
  int y_inv = ~y;
  int r = x_inv & y_inv;
  return l | r;
}
/* 
 * allOddBits - return 1 if all odd-numbered bits in word set to 1
 *   where bits are numbered from 0 (least significant) to 31 (most significant)
 *   Examples allOddBits(0xFFFFFFFD) = 0, allOddBits(0xAAAAAAAA) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 2
 */
int allOddBits(int x) {
  int mask = 0xAA; // constant 0b0000... ....10101010
  // extending the mask to be 32 bits
  // utilizing the fact that mask has 24 zero's after the rightmost 1
  // therefore, the OR operation can extend the mask
  mask |= (mask << 8); // The mask is now 16 bits of non-trivial data
  mask |= (mask << 16); // The mask is now 32 bits, with a 1 at each odd bit
  
  int oddBits = x & mask;
  int result = oddBits ^ mask; // If any odd bits are zero, then this number is not equal to zero
  return !result; // If the number is zero, then return true, otherwise, return false
}
/* 
 * byteSwap - swaps the nth byte and the mth byte
 *  Examples: byteSwap(0x12345678, 1, 3) = 0x56341278
 *            byteSwap(0xDEADBEEF, 0, 2) = 0xDEEFBEAD
 *  You may assume that 0 <= n <= 3, 0 <= m <= 3
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 25
 *  Rating: 2
 */
int byteSwap(int x, int n, int m) {
  int nShift = n << 3;
  int mShift = m << 3;
  int xn = x >> nShift;
  int xm = x >> mShift;
  int ones = 0xFF;
  int xored = xn ^ xm;
  int swapped = ones & xored;
  int nSwapShifted = swapped << nShift;
  int mSwapShifted = swapped << mShift;
  int result = x ^ nSwapShifted;
  result = result ^ mSwapShifted;
  return result;
}
/* 
 * isGreater - if x > y  then return 1, else return 0 
 *   Example: isGreater(4,5) = 0, isGreater(5,4) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isGreater(int x, int y) {
  int differenceMask = x ^ y;
  
  // Propogate the mask to give a new mask of values that we need to check
  differenceMask |= differenceMask >> 1;
  differenceMask |= differenceMask >> 2;
  differenceMask |= differenceMask >> 4;
  differenceMask |= differenceMask >> 8;
  differenceMask |= differenceMask >> 16;

  // Set the signbit of the mask to one, but leave the rest unchanged
  // differenceMask |= (1 << 31);
  int signMask = (1 << 31);
  int signMaskInv = ~signMask;

  differenceMask &= ~(differenceMask >> 1) | signMask;
  differenceMask &= (x ^ signMask) & (y ^ signMaskInv);

  return !!differenceMask;
}
/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {
  int shifted4 = x >> 4;
  int shifted3 = x >> 3;
  int shifted1 = x >> 1;
  int l = shifted4 ^ 0x3;
  int r1 = shifted3 & 1;
  int r2 = !(shifted1 & 0x3);
  int r = r1 & !r2;
  int result = !l & !r;
  return result;
}
/*
 * bitParity - returns 1 if x contains an odd number of 0's
 *   Examples: bitParity(5) = 0, bitParity(7) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int bitParity(int x) {
  // Takes advantage that the addition and odds and evens follow the same pattern as an XOR gate
  // even + even = even
  // even + odd = odd
  // odd + even = odd
  // odd + odd = even

  // Collapse the "evenness" or "oddness" of neighboring bits onto one bit
  int y = x ^ (x >> 1); // Condensing neighbor from right
  y = y ^ (y >> 2); // Condensing neighbor two to the right
  y = y ^ (y >> 4); // etc...
  y = y ^ (y >> 8);
  y = y ^ (y >> 16);
  // Only have to do this in this specified pattern as each "compression" divides the amount of bits that have an accurate sum by 2
  // Log_2 of 32 is 5 steps...
  // After 5 steps, the "evenness" can be found on the least significant bit
  // & 1 to cut all but the lsb off
  return y & 1;
}
/*
 * isTmin - returns 1 if x is the minimum, two's complement number,
 *     and 0 otherwise 
 *   Legal ops: ! ~ & ^ | +
 *   Max ops: 10
 *   Rating: 1
 */
int isTmin(int x) {
  int zero = !x;
  int r = ~x + 1;
  int l = x^r;
  int result = !(l + zero);
  return result;
}
/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
  int negative = x >> 31;
  int sign = negative << 31;
  int l = x ^ negative;
  int r = negative & 1;
  int magnitude = l + r;
  int result = sign | magnitude;
  return result;
}
